import math

# 최대공약수
a = math.gcd(60, 100, 80)
print(a)

# 최소공배수
b = math.lcm(15, 25, 5)
print(b)    

# 원주율
c = math.pi
print(c)

# 자연상수
d = math.e
print(d)

# 제곱근
e = math.sqrt(2)
print(e)

# 거듭제곱
f = math.pow(2, 3)
print(f)

# 자연로그
g = math.log(10)
print(g)

# 상용로그
h = math.log10(10)
print(h)

# 이진로그
i = math.log2(10)
print(i)